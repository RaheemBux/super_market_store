    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        CustomerDealer customerDealer = this.getCustomerDealerData();
        customerDealer.setCustomerDealerId(customerDealerId);
        boolean success = customerDealerDao.deleteCustomerDealer(customerDealerId);
        if (success) {
            JOptionPane.showMessageDialog(this, "Deleted Successfully");
            fillCustomerDealerTable();
            clearFields();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if (checkFields()) {
            if (validatePhoneNumber()) {
                boolean success = customerDealerDao.addCustomerDealer(getCustomerDealerData());
                if (success) {
                    JOptionPane.showMessageDialog(this, "Added Successfully");
                    fillCustomerDealerTable();
                    clearFields();
                }
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        CustomerDealer customerDealer = this.getCustomerDealerData();
        customerDealer.setCustomerDealerId(customerDealerId);
        boolean success = customerDealerDao.updateCustomerDealer(customerDealer);
        if (validatePhoneNumber()) {
            if (success) {
                JOptionPane.showMessageDialog(this, "Updated Successfully");
                fillCustomerDealerTable();
                clearFields();
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void customerDelaerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerDelaerTableMouseClicked
        // TODO add your handling code here:
        customerDealerId = Integer.parseInt(customerDelaerTable.getValueAt(customerDelaerTable.getSelectedRow(), 0).toString());
        CustomerDealer customerDealer = customerDealerDao.getCustomerDealerById(customerDealerId);
        nameField.setText(customerDealer.getCustomerDealerName());
        emailField.setText(customerDealer.getEmail());
        phoneNumberField.setText(customerDealer.getPhoneNumber());
        addressField.setText(customerDealer.getAddress());
        typeComboBox.setSelectedItem(customerDealer.getCustomerDealerEnum());
    }//GEN-LAST:event_customerDelaerTableMouseClicked

