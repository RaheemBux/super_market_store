    private boolean validatePhoneNumber() {
        if (!Pattern.matches("^[0-9]+$", phoneNumberField.getText())) {
            JOptionPane.showMessageDialog(this, " Invalid Phone Number \n Phone Number Should Contain only numbers");
            return false;
        }
        return true;
    }

    private CustomerDealer getCustomerDealerData() {

        String name = nameField.getText();
        Enum type = (Enum) typeComboBox.getSelectedItem();
        String email = emailField.getText();
        String address = addressField.getText();
        String phoneNumber = phoneNumberField.getText();
        long millis = System.currentTimeMillis();
        java.sql.Date currentDate = new java.sql.Date(millis);
        java.sql.Date modifedDate = new java.sql.Date(millis);
        CustomerDealer customerDealer = new CustomerDealer();
        customerDealer.setCustomerDealerName(name);
        customerDealer.setAddress(address);
        customerDealer.setCustomerDealerEnum((CustomerDealerEnum) type);
        customerDealer.setEmail(email);
        customerDealer.setPhoneNumber(phoneNumber);
        customerDealer.setCreateDate(currentDate);
        customerDealer.setModifiedDate(modifedDate);
        customerDealer.setActive(true);
        customerDealer.setCreatedBy("SMS");
        customerDealer.setUpdatedBy("SMS");
        return customerDealer;

    }

    private boolean checkFields() {
        if ("".equals(phoneNumberField.getText()) || "".equals(nameField.getText()) || "".equals(emailField.getText()) || "".equals(addressField.getText()) || typeComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "All Fields Must Be Filled!!!!");
            return false;
        }
        return true;
    }

